#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	w_number(int a)
{
	char decimal;
	char unite;

	decimal = a / 10 + '0';
	unite = a % 10 + '0';
	ft_putchar(decimal);
	ft_putchar(unite);
}

void	ft_print_comb2(void)
{
	int a;
	int b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			w_number(a);
			ft_putchar(' ');
			w_number(b);
			if (a != 98)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			b++;
		}
		a++;
	}
}
