#include <unistd.h>

void	nbr_rec(int n)
{
	char m;

	if (n > 9)
	{
		m = n % 10 + '0';
		nbr_rec(n / 10);
	}
	else
		m = n + 48;
	write(1, &m, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			nb = -nb;
			write(1, "-", 1);
		}
		nbr_rec(nb);
	}
}
