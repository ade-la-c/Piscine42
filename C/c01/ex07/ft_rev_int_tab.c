void	ft_rev_int_tab(int *tab, int size)
{
	int		swap;
	int		i;
	int		j;

	i = 0;
	j = size - 1;
	while (i < (size / 2))
	{
		swap = tab[i];
		tab[i] = tab[j];
		tab[j] = swap;
		i++;
		j--;
	}
}
